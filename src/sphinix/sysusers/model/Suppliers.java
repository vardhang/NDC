package sphinix.sysusers.model;

public class Suppliers {
	private int supplierId;
	private String supplierName;
	private String supplierDetails;
	private String supplierContactNo;
	private String supplierContactPerson;
	
	public int getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(int supplierId) {
		this.supplierId = supplierId;
	}
	public String getSupplierName() {
		return supplierName;
	}
	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}
	public String getSupplierDetails() {
		return supplierDetails;
	}
	public void setSupplierDetails(String supplierDetails) {
		this.supplierDetails = supplierDetails;
	}
	public String getSupplierContactNo() {
		return supplierContactNo;
	}
	public void setSupplierContactNo(String supplierContactNo) {
		this.supplierContactNo = supplierContactNo;
	}
	public String getSupplierContactPerson() {
		return supplierContactPerson;
	}
	public void setSupplierContactPerson(String supplierContactPerson) {
		this.supplierContactPerson = supplierContactPerson;
	}

}
